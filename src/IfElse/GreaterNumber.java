package IfElse;

public class GreaterNumber {
    public static void main(String[] args) {
        int a=100;
        int b=47;

        if(a>b){
            System.out.println("a is greater number.");
            System.out.println("a = "+a);
        }
        else{
            System.out.println("b is greater number.");
            System.out.println("b = "+b);
        }

    }
}
